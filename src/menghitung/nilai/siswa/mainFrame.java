    private void selectData() {
        String kolom[] = {"NIS","NamaSiswa","Kelas","Mapel","NilaiTugas","NilaiUTS","NilaiUAS","NilaiAkhir","NilaiHuruf","Keterangan"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_nilai";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NIS = rs.getString(1);
                String NamaSiswa = rs.getString(2);
                String Kelas = rs.getString(3);
                String Mapel = rs.getString(4);
                String NilaiTugas = rs.getString(5);         
                String NilaiUTS = rs.getString(6);
                String NilaiUAS = rs.getString(7);
                String NilaiAkhir = rs.getString(8);
                String NilaiHuruf = rs.getString(9);
                String Keterangan = rs.getString(10);
                String data[] = {NIS,NamaSiswa,Kelas,Mapel,NilaiTugas,NilaiUTS,NilaiUAS,NilaiAkhir,NilaiHuruf,Keterangan};
               dtm.addRow(data);
            }
        }catch(SQLException ex){
            Logger.getLogger(mainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbNilai.setModel(dtm);
    }
