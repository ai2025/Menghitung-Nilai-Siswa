    private void btSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSimpanActionPerformed
        // TODO add your handling code here:
        if("".equals(txtnis.getText()) || 
                "".equals(txtnasis.getText()) || 
                "".equals(txtkelas.getText()) || 
                "".equals(txttugas.getText()) ||
                "".equals(txtuts.getText()) ||                
                "".equals(txtuas.getText())){
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data!", 
                    "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            String SQL = "INSERT INTO tb_nilai VALUES ('"+txtnis.getText()+"','"
                    +txtnasis.getText()+"','"+txtkelas.getText()+"','"
                    +cbMapel.getSelectedItem()+"','"+txttugas.getText()+"','"+txtuts.getText()+"','"
                    +txtuas.getText()+"','"+txtakhir.getText()+"','"+txthuruf.getText()+"','"
                    +txtket.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Ditambahkan", "SUCCESS",
                        JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Ditambahkan", "FAILED",
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btSimpanActionPerformed

