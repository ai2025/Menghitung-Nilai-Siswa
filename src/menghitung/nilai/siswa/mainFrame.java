    private void btHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btHitungActionPerformed
        // TODO add your handling code here:
        tugas = Float.parseFloat(txttugas.getText());
        uas = Float.parseFloat(txtuas.getText());
        uts = Float.parseFloat(txtuts.getText());
        akhir = ((tugas*40/100)+(uts*30/100)+(uas*30/100));
        txtakhir.setText(Float.toString(akhir));
        
        if (akhir>=80){
            txthuruf.setText("A");
            txtket.setText("LULUS");
        }else if(akhir>=70){
            txthuruf.setText("B");
            txtket.setText("LULUS");
        }else if(akhir>=60){
            txthuruf.setText("C");
            txtket.setText("REMIDI");
        }else if(akhir>=40){
            txthuruf.setText("D");
            txtket.setText("REMIDI");
        }else if(akhir<40){
            txthuruf.setText("E");
            txtket.setText("TIDAK LULUS");
        }
    }//GEN-LAST:event_btHitungActionPerformed

